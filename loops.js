//for loop
for (let i = 0; i < 10 ; i++) {
    console.log(i+1,"ALok");
}

//while loop
let j = 0;
while(j<10) {
    console.log(j+1, "Alok");
    j += 1;
}

//do_while loop
let i = 0;
do {
    console.log("Hello",i);
    i++ ;
}
while (i<10);

